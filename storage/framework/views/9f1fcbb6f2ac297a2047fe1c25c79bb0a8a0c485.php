    4 buah cabai merah besar;
    ½ sdm ketumbar;
    2 cm jahe;
    2 cm kunyit;
    1 sdm garam;

    <h3>Cara membuat</h3>
    
1. Haluskan kemiri, bawang merah, bawang putih, cabai merah, jahe, dan kunyit. Masukkan bumbu yang sudah dihaluskan ke dalam wadah. Tambahkan garam ketumbar, merica, dan Bango Kecap Manis. Aduk rata.
2. Masukkan ayam yang telah dibersihkan. Baluri seluruh permukaan ayam dengan bumbu.Diamkan selama satu jam di dalam kulkas.
3. Siapkan pembakaran. Panggang ayam yang telah dibumbui. Gunakan sisa bumbu marinasi sebagai alat olesan.
4. Olesi sesekali seuruh permukaan ayam. Angkat setelah matang.
5. Hidangkan selagi hangat dengan nasi dan sambal.

